# 2 ćwiczenie

a = input("Podaj pierwszą liczbę całkowitą: ")
b = input("Podaj drugą liczbę całkowitą: ")

print("Wynik dodawania tych licz to:", int(a) + int(b))
print("Wynik odejmowania tych licz to:",int(a) - int(b))
print("Wynik mnożenia tych licz to:",int(a) * int(b))
print("Wynik dzielenia tych licz to:",int(a) / int(b))
print("Reszta z dzielenia:",int(a) % int(b))

print()

